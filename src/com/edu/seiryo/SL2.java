package com.edu.seiryo;

public class SL2 {
	public static void main(String[] args) {
		//输出不换行，使用空格分割字符
		System.out.print("你好！");
		//通过换行符"\n"换行
		System.out.print("青凌！!\n");
		System.out.println("-----------------");
		System.out.print("你好");
		//通过制表符"\t"定位显示数据，与Tab键的定位效果相似
		System.out.println("\t青凌！");
	}
}
